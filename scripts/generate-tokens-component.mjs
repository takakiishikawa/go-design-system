/**
 * tokens.css / globals.css の内容を読んで
 * src/components/ui/design-tokens-css.ts を生成する。
 * build コマンドの前に実行すること。
 */
import { readFileSync, writeFileSync } from "fs"
import { fileURLToPath } from "url"
import path from "path"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, "..")

const tokensCSS = readFileSync(path.join(root, "src/styles/tokens.css"), "utf8")
const globalsCSS = readFileSync(path.join(root, "src/styles/globals.css"), "utf8")

const output = `// AUTO-GENERATED by scripts/generate-tokens-component.mjs
// tokens.css または globals.css を変更した後は npm run build を実行すること。
// このファイルを直接編集しないこと。

export const TOKENS_CSS = ${JSON.stringify(tokensCSS)};

export const GLOBALS_CSS = ${JSON.stringify(globalsCSS)};
`

writeFileSync(
  path.join(root, "src/components/ui/design-tokens-css.ts"),
  output
)
console.log("Generated src/components/ui/design-tokens-css.ts")
